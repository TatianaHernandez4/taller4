//Escribe una función que tome dos números como parámetros y devuelva su suma.
// Define la función para sumar dos números
function sum(a, b) {
    return a + b;
}

// Ejemplo de uso de la función
const num1 = 3;
const num2 = 5;
const resultado = sum(num1, num2);

console.log(`La suma de ${num1} y ${num2} es: ${resultado}`);
